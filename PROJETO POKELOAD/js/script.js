  function imagem_aleatoria() {
    var p, n, tmp, destino = document.querySelector('#imgdest');
    var img_len = img_list.length;
    for (p = img_len; p;) {
      n = Math.random() * p-- | 0;
      tmp = img_list[n];
      img_list[n] = img_list[p];
      img_list[p] = tmp;
      tmp = nome_list[n];
      nome_list[n] = nome_list[p];
      nome_list[p] = tmp;
    }
  
    var nomeAnterior = document.querySelector('#nomePokemon');
    if (nomeAnterior) {
      nomeAnterior.remove();
    }
  
    var nome = document.createElement('p');
    nome.id = "nomePokemon";
    nome.innerHTML = nome_list[0]; // Define o texto do parágrafo com o nome do pokemon.
    nome.classList.add("nome-pokemon"); // Adicione uma classe CSS.
    destino.parentElement.appendChild(nome); // Adiciona o parágrafo como um irmão do elemento de imagem.
  
    destino.src = img_list[0];
  }
  